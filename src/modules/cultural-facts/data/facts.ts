export function getFactItems(difficulty: FactsDifficulty): CulturalFact[] {
  return difficulty === 'easy' ? FACTS.filter((fact) => fact.tier === 1) : FACTS;
}

export function generateFactOptions(correct: CulturalFact, pool: CulturalFact[]): CulturalFact[] {
  const sameCategoryWrong = pool.filter(
    (item) => item.id !== correct.id && item.category === correct.category,
  );
  const otherWrong = pool.filter(
    (item) => item.id !== correct.id && item.category !== correct.category,
  );
  const distractors = [...sameCategoryWrong, ...otherWrong].slice(0, OPTION_COUNT - 1);

  return [correct, ...distractors].sort(() => Math.random() - SHUFFLE_RANDOM_OFFSET);
}
